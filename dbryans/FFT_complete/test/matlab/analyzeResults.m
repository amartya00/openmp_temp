names={ ...
    'fft2d_1024x1024' ...
    'fft2d_512x512' ...
    'fft2d_1024x512' ...
    'fft2d_256x256' ...
    'fft2d_2048x2048' ...
    'fft1d_512x512' ...
    'fft1d_1024x1024' ...
    'fft1d_2048x16' ...
    'fft1d_2048x128' ...
    'fft1d_2048x2048' ...
};

for cnt=1:length(names),
    disp(sprintf('analyzing.... %s',names{cnt})); 
    filename=sprintf('../vectors/out/%s.bin',names{cnt});    
    fid=fopen(filename,'rb');
    f=fread(fid,'float32');
    fclose(fid);
    filename=sprintf('../vectors/refout/%s.bin',names{cnt});    
    fid=fopen(filename,'rb');
    fr=fread(fid,'float32');
    fclose(fid);
    if(length(f) ~= length(fr)) disp('file size mismatch!');
    else
     er=f-fr;
     dBEr = 20*log10(norm(er)/norm(fr));
     if(dBEr > -125) disp('data mismatch!!'); end
    end;
end;

